    npcs.base.autonpcfunctions.AddCycleText("Zsss!","Zsss!");
    npcs.base.autonpcfunctions.AddCycleText("Zsss?","Zsss?");
    npcs.base.autonpcfunctions.AddCycleText("#me zischelt.","#me hisses.");
